    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        
        //Validate characters
        char ls_char;
        int li_ascii;
        try {
            ls_char = evt.getKeyChar();
            li_ascii = Character.toString((char) ls_char).codePointAt(0);
        } catch (Exception err1) {
            li_ascii = 0;
        }
        //JOptionPane.showMessageDialog(null, "li_ascii: " + li_ascii, "Error", JOptionPane.ERROR_MESSAGE);
        
        if (li_ascii > 90) {
            evt.consume();
        } else if ((li_ascii < 48) && (li_ascii != 32)) {
            evt.consume();
        } else if ((li_ascii > 57) && (li_ascii < 65)) {
            evt.consume();
        }

        //Set a MaxLimit
        String ls_text = jTextField1.getText();
        int length = ls_text.length();
        if (length == 20) {
            evt.consume();
        } else if (length > 20) {
            // show your error message
            JOptionPane.showMessageDialog(null, "Error. No se permiten mas de 20 caracteres: ", "Restricción", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText(ls_text.substring(0, 20));
        }
    }//GEN-LAST:event_jTextField1KeyTyped

