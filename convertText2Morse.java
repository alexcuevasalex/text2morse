    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        //MORSE TO TEXTO
        String ls_morse = jTextField2.getText();
        
        if (ls_morse.length() > 0) {
            String ls_char;
            String ls_texto = "";
            String ls_charMorse = "";
            int li_pos = 0;
            int li_index;
            int li_count_espacios = 0;
            boolean lb_continuar = false;

            try {
                while (li_pos < ls_morse.length()) {
                    ls_char = ls_morse.substring(li_pos, (li_pos + 1));
                    //Validacion de espacios
                    if (ls_char.equals(" ")) {
                        if (lb_continuar && (0 == li_count_espacios)) {
                            //buscar charMorse
                            li_index = 0;
                            while (li_index < 36) {
                                if (ls_charMorse.equals(ps_equivalentes[li_index])) {
                                    ls_texto += ps_abcTexto.substring(li_index, (li_index + 1));
                                    li_index = 35;
                                }
                                li_index++;
                            }
                            ls_charMorse = "";//Afuera, si no la encuentra, no se limpia
                        } else if (lb_continuar && (2 == li_count_espacios)) {
                            ls_texto += " ";
                        }
                        li_count_espacios++;
                    } else {
                        ls_charMorse += ls_char;
                        lb_continuar = true;
                        li_count_espacios = 0;
                    }
                    li_pos++;
                }
                if (lb_continuar && (0 == li_count_espacios) && (ls_charMorse.length() > 0)) {
                    //Tomando el ultimo caracter
                    li_index = 0;
                    while (li_index < 36) {
                        if (ls_charMorse.equals(ps_equivalentes[li_index])) {
                            ls_texto += ps_abcTexto.substring(li_index, (li_index + 1));
                            li_index = 35;
                        }
                        li_index++;
                    }
                }
            } catch (Exception err1) {
                ls_texto = "ERROR: " + err1.toString();
            }

            if (ls_texto.length() == 0) {
                ls_texto = "Cadena no valida";
            }
            jTextField1.setText(ls_texto);
        }        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //TEXTO TO MORSE
        String ls_texto = jTextField1.getText();
        
        if (ls_texto.length() > 0) {
            String ls_char;
            String ls_morse = "";
            int li_pos = 0;
            int li_index;
            int li_count_espacios = 0;
            boolean lb_continuar = false;

            try {
                while (li_pos < ls_texto.length()) {
                    ls_char = ls_texto.substring(li_pos, (li_pos + 1));
                    //Validacion de espacios
                    if (ls_char.equals(" ")) {
                        if (lb_continuar && (0 == li_count_espacios)) {
                            ls_morse += "  ";
                            li_count_espacios++;
                        }
                    } else {
                        li_index = ps_abcTexto.indexOf(ls_char);
                        ls_morse += ps_equivalentes[li_index] + " ";
                        lb_continuar = true;
                        li_count_espacios = 0;
                    }
                    li_pos++;
                }
            } catch (Exception err1) {
                ls_morse = "ERROR: " + err1.toString();
            }

            jTextField2.setText(ls_morse);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

